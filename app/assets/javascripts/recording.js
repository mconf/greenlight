// Handle changing of settings tabs.
$(document).on('turbolinks:load', function(){
  var controller = $("body").data('controller');
  var action = $("body").data('action');

  if (controller == "rooms" && action == "show" 
      || controller == "rooms" && action == "update" 
      || controller == "users" && action == "recordings" 
      || controller == "admins" && action == "server_recordings"){
    // Handle recording emails.
    $('.email-link').each(function(){
      $(this).click(function(){
        var subject = $(".username").text() + " " + getLocalizedString('javascript.room.mailer.subject');
        var body =  getLocalizedString('javascript.room.mailer.body') + "\n\n" + $(this).attr("data-pres-link");
        var autogenerated = getLocalizedString('javascript.room.mailer.autogenerated') + "\n";
        var footer = getLocalizedString('javascript.room.mailer.footer');

        var url = "mailto:?subject=" + encodeURIComponent(subject) + "&body=" + encodeURIComponent(body) + encodeURIComponent(autogenerated) + encodeURIComponent(footer);
        var win = window.open(url, '_blank');

        win.focus();
      });
    });

    // Handle recording clipboard copy
    function copyTextToClipboard(text) {
      const textArea = document.createElement("textarea");
      textArea.value = text;
      document.body.appendChild(textArea);
      textArea.select();
      let successful = false;

      try {
        sucessfull = document.execCommand('copy');
      } catch (err) {
        sucessfull = false;
      }

      document.body.removeChild(textArea);

      return sucessfull;
    }

    // Handle recording cliboard copy.
    $('.clipboard-link').each(function() {
      const clipboard_link = $(this);

      clipboard_link.click(() => {
        const clipboard_text = clipboard_link.attr("data-pres-link");
        const success = copyTextToClipboard(clipboard_text);

        if (success) {
          clipboard_link.removeClass('btn-secondary')
          clipboard_link.addClass('btn-success text-white');
          clipboard_link.html("<i class='fas fa-check'></i>")

          setTimeout(() => {
            clipboard_link.addClass('btn-secondary');
            clipboard_link.removeClass('btn-success text-white');
            clipboard_link.html("<i class='fas fa-copy'></i>")
          }, 2000)
        }
      });
    });
  }
});